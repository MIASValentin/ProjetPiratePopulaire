    private void formMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_formMouseReleased
        Point location = getLocation();
        int xCentreCarte = location.x + getWidth()/2;
        int yCentreCarte = location.y + getHeight()/2;
        
        if (zoneHand.getBounds().contains(new Point(xCentreCarte, yCentreCarte))){
            //toujours dans hand
            zoneHand.add(this);
        }else{
            //jouer la carte
        }
        
    }//GEN-LAST:event_formMouseReleased

    private void formMouseDragged(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_formMouseDragged
        if (moving) {
            Point newPos = new Point(evt.getPoint().x-posPrec.x, evt.getPoint().y-posPrec.y);
            setLocation(getLocation().x+newPos.x, getLocation().y+newPos.y);
        }
    }//GEN-LAST:event_formMouseDragged


